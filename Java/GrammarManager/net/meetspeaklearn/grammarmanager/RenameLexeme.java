    private void newLexemeNameTextFieldKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_newLexemeNameTextFieldKeyReleased
        String text = newLexemeNameTextField.getText();

        if (lexicon.isInterned(text)) {
            newLexemeNameTextField.setForeground(Color.red);
            createButton.setEnabled(false);
        } else {
            newLexemeNameTextField.setForeground(Color.black);
            createButton.setEnabled(true);
        }
    }//GEN-LAST:event_newLexemeNameTextFieldKeyReleased

