    private void createButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_createButtonActionPerformed
        String text = newLexemeNameTextField.getText();
        String messageIfFailed = lexicon.rename(lexeme, text);

        if (messageIfFailed != null) {
            showMessageDialog(this, messageIfFailed);
            newLexemeNameTextField.selectAll();
            createButton.setEnabled(false);
            return;
        } else {
            createButton.setEnabled(true);
        }

        if (gm != null) {
            gm.updateView();
            gm.selectLexeme(lexeme);
        }

        setVisible(false);
        dispose();
    }//GEN-LAST:event_createButtonActionPerformed

